class TimestepEmbedder(nn.Module):
    """
    Embeds scalar timesteps into vector representations.
    """
    def __init__(self, hidden_size, frequency_embedding_size=256):
        super().__init__()
        self.mlp = nn.Sequential(
            nn.Linear(frequency_embedding_size, hidden_size, bias=True),
            nn.SiLU(),
            nn.Linear(hidden_size, hidden_size, bias=True),
        )
        self.frequency_embedding_size = frequency_embedding_size

    @staticmethod
    def timestep_embedding(t, dim, max_period=10000):
        """
        Create sinusoidal timestep embeddings.
        :param t: a 1-D Tensor of N indices, one per batch element.
                          These may be fractional.
        :param dim: the dimension of the output.
        :param max_period: controls the minimum frequency of the embeddings.
        :return: an (N, D) Tensor of positional embeddings.
        """
        # https://github.com/openai/glide-text2im/blob/main/glide_text2im/nn.py
        half = dim // 2
        freqs = torch.exp(
            -math.log(max_period) * torch.arange(start=0, end=half, dtype=torch.float32) / half
        ).to(device=t.device)
        args = t[:, None].float() * freqs[None]
        embedding = torch.cat([torch.cos(args), torch.sin(args)], dim=-1)
        if dim % 2:
            embedding = torch.cat([embedding, torch.zeros_like(embedding[:, :1])], dim=-1)
        return embedding

    def forward(self, t):
        t_freq = self.timestep_embedding(t, self.frequency_embedding_size)
        t_emb = self.mlp(t_freq)
        return t_emb

class GuidanceEmbeddingModule(nn.Module):
    """
    Replaces LabelEmbedder. Uses precomputed class-mean encoder embeddings
    passed through learned MLPs to produce guidance embeddings for conditioning.

    Handles label dropout for classifier-free guidance by mapping dropped labels
    to the NTC (null) class index.
    """
    def __init__(self, num_classes, hidden_size, dropout_prob=0.1, embed_dim=384):
        super().__init__()
        self.num_classes = num_classes
        self.dropout_prob = dropout_prob
        self.embed_dim = embed_dim

        self.register_buffer('class_means', torch.zeros(num_classes + 1, embed_dim))

        self.mu_phi = nn.Sequential(
            nn.Linear(embed_dim, hidden_size, bias=True),
            nn.ReLU(),
            nn.Linear(hidden_size, hidden_size, bias=True),
        )
        self.sigma_phi = nn.Sequential(
            nn.Linear(embed_dim, hidden_size, bias=True),
            nn.ReLU(),
            nn.Linear(hidden_size, hidden_size, bias=True),
            nn.ReLU(),  # Ensure sigma is positive
        )

        self.register_buffer('mu_eta', torch.zeros(hidden_size))
        self.register_buffer('sigma_sq_eta', torch.ones(hidden_size))

    def token_drop(self, labels, force_drop_ids=None):
        if force_drop_ids is None:
            drop_ids = torch.rand(labels.shape[0], device=labels.device) < self.dropout_prob
        else:
            drop_ids = force_drop_ids == 1
        return torch.where(drop_ids, self.num_classes, labels)

    def forward(self, labels, train=True, force_drop_ids=None):
        use_dropout = self.dropout_prob > 0
        if (train and use_dropout) or (force_drop_ids is not None):
            labels = self.token_drop(labels, force_drop_ids)

        class_mean = self.class_means[labels]

        mu_c = self.mu_phi(class_mean)
        # sigma_c = torch.nn.functional.softplus(self.sigma_phi(class_mean))
        sigma_c = self.sigma_phi(class_mean)

        null_mask = (labels == self.num_classes)
        sigma_c = sigma_c * (~null_mask).unsqueeze(-1).float()

        noise = torch.randn_like(mu_c)
        e_c = mu_c + sigma_c * noise

        return e_c

    def forward_deterministic(self, labels):
        class_mean = self.class_means[labels]
        mu_c = self.mu_phi(class_mean)
        return mu_c

    @torch.no_grad()
    def update_empirical_bayes(self):
        all_means = self.class_means[:-1]
        mu_all = self.mu_phi(all_means)
        # sigma_all = torch.nn.functional.softplus(self.sigma_phi(all_means))
        sigma_all = self.sigma_phi(all_means)

        self.mu_eta.copy_(mu_all.mean(dim=0))
        diff = mu_all - self.mu_eta
        self.sigma_sq_eta.copy_((diff ** 2 + sigma_all ** 2).mean(dim=0))
        # dist.breakpoint()

    def kl_loss(self, mu_c, sigma_c, labels):
        non_null_mask = (labels != self.num_classes)
        if non_null_mask.sum() == 0:
            return torch.tensor(0.0, device=mu_c.device)

        mu_nn = mu_c[non_null_mask]
        sigma_nn = sigma_c[non_null_mask]

        eps = 1e-6
        var_c = sigma_nn ** 2 + eps

        kl = 0.5 * (
            torch.sum(torch.log((self.sigma_sq_eta + eps) / var_c), dim=-1)
            - mu_nn.shape[-1]
            + torch.sum(var_c / (self.sigma_sq_eta + eps), dim=-1)
            + torch.sum((mu_nn - self.mu_eta) ** 2 / (self.sigma_sq_eta + eps), dim=-1)
        )
        return kl.mean()


class DirectEmbeddingModule(nn.Module):
    """
    Minimal conditioning module. Directly uses precomputed class-mean encoder
    embeddings as the conditioning vector with zero trainable parameters.

    Label dropout still works: dropped labels map to NTC index, which returns
    the NTC embedding.
    """
    def __init__(self, num_classes, hidden_size, dropout_prob=0.1, embed_dim=384):
        super().__init__()
        self.num_classes = num_classes
        self.dropout_prob = dropout_prob
        self.embed_dim = embed_dim

        self.register_buffer('class_means', torch.zeros(num_classes + 1, embed_dim))

    def token_drop(self, labels, force_drop_ids=None):
        if force_drop_ids is None:
            drop_ids = torch.rand(labels.shape[0], device=labels.device) < self.dropout_prob
        else:
            drop_ids = force_drop_ids == 1
        return torch.where(drop_ids, self.num_classes, labels)

    def forward(self, labels, train=True, force_drop_ids=None):
        use_dropout = self.dropout_prob > 0
        if (train and use_dropout) or (force_drop_ids is not None):
            labels = self.token_drop(labels, force_drop_ids)

        return self.class_means[labels]

    def kl_loss(self, *args, **kwargs):
        return torch.tensor(0.0)


class LabelEmbedder(nn.Module):
    """
    Embeds class labels into vector representations. Also handles label dropout for classifier-free guidance.
    """
    def __init__(self, num_classes, hidden_size, dropout_prob):
        super().__init__()
        use_cfg_embedding = dropout_prob > 0
        self.embedding_table = nn.Embedding(num_classes + use_cfg_embedding, hidden_size)
        self.num_classes = num_classes
        self.dropout_prob = dropout_prob

    def token_drop(self, labels, force_drop_ids=None):
        """
        Drops labels to enable classifier-free guidance.
        """
        if force_drop_ids is None:
            drop_ids = torch.rand(labels.shape[0], device=labels.device) < self.dropout_prob
        else:
            drop_ids = force_drop_ids == 1
        labels = torch.where(drop_ids, self.num_classes, labels)
        return labels

    def forward(self, labels, train, force_drop_ids=None):
        use_dropout = self.dropout_prob > 0
        if (train and use_dropout) or (force_drop_ids is not None):
            labels = self.token_drop(labels, force_drop_ids)
        embeddings = self.embedding_table(labels)
        return embeddings


